a=int(input("n: "))
if a<0:
    print("__Multiplication Table upto_",a,":")
    print("--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------")
    for i in range (-1, a-1, -1):
        print("Table of_",i,":")
        for b in range (1,11):
            print(i,"*",b,"=",i*b)
        print( )
elif (a>0):
    print("__Multiplication Table upto_",a,":")
    print("--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------")
    for i in range(1,a+1):
        print("Table of_",i,":")
        for b in range (1,11):
            print(i,"*",b,"=",i*b)
        print( )
else:
    print("Multiplication Table of",a,":")
    print(0)
